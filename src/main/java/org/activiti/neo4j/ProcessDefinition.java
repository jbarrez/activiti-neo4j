package org.activiti.neo4j;


public class ProcessDefinition {
  
  protected String id;
  protected String key;
  
  public String getId() {
    return id;
  }
  
  public void setId(String id) {
    this.id = id;
  }
  
  public String getKey() {
    return key;
  }
  
  public void setKey(String key) {
    this.key = key;
  }
  
}
